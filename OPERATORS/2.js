// The comma operator (,) in JavaScript is a binary operator that evaluates its operands from left to right and returns the value of the rightmost operand. It is often used to execute multiple expressions in a place where only one expression is expected. For example, the following code uses the comma operator to increment two variables at once:

var i = 5, j = 10;
i++, j++;
console.log(i);
console.log(j);