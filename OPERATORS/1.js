// The ternary operator is a conditional operator that takes three operands: a condition, a value to return if the condition is true, and a value to return if the condition is false. It is often used as a shorthand for an if-else statement.

// condition ? value_if_true : value_if_false

var num = 10;
var b = num % 2 == 0 ? 'EVEN' : 'ODD';  
console.log(b);